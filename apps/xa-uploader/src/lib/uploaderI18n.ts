export type UploaderMessageKey = keyof (typeof messages)["en"];

export function getUploaderMessage(locale: UploaderLocale, key: UploaderMessageKey): string {
  const bundle = messages[locale] ?? messages[UPLOADER_DEFAULT_LOCALE];
  return bundle[key] ?? messages.en[key];
}

export function getUploaderConfirmDelete(locale: UploaderLocale, slug: string): string {
  return locale === "zh" ? `删除 ${slug}？` : `Delete ${slug}?`;
}
