import { useCallback } from "react";
import { get, ref } from "firebase/database";

import { useFirebaseDatabase } from "../../services/useFirebase";
import type { MutationState } from "../../types/hooks/mutations/mutationState";

import useActivitiesMutations from "./useActivitiesMutations";
import useArchiveBooking from "./useArchiveBooking";
import useMutationState from "./useMutationState";

interface CancellationActivityFailure {
  occupantId: string;
  error: string;
}

export interface CancelBookingResult {
  success: boolean;
  bookingRef: string;
  archived: boolean;
  activityFailures: CancellationActivityFailure[];
}

export class CancellationPartialFailureError extends Error {
  readonly bookingRef: string;
  readonly activityFailures: CancellationActivityFailure[];

  constructor(bookingRef: string, activityFailures: CancellationActivityFailure[]) {
    super(
      `Cancellation archived but ${activityFailures.length} occupant activity update(s) failed`
    );
    this.name = "CancellationPartialFailureError";
    this.bookingRef = bookingRef;
    this.activityFailures = activityFailures;
  }
}

/**
 * Return type for the useCancelBooking hook.
 */
interface UseCancelBookingReturn extends MutationState<void> {
  cancelBooking: (bookingRef: string, reason?: string) => Promise<CancelBookingResult>;
}

/**
 * useCancelBooking:
 * Orchestrates the complete booking cancellation workflow.
 *
 * Workflow:
 *  1. Write status="cancelled" to /bookingMeta via useArchiveBooking
 *  2. Enumerate occupants from /bookings/{ref}
 *  3. Log activity code 27 (CANCELLED) for EACH occupant via useActivitiesMutations
 *  4. Keep guestsByBooking/{occupantId} intact (NOT deleted)
 *
 * Email drafts are auto-generated by useActivitiesMutations when code 27 is logged
 * (code 27 is in relevantCodes).
 */
export default function useCancelBooking(): UseCancelBookingReturn {
  const database = useFirebaseDatabase();
  const { archiveBooking } = useArchiveBooking();
  const { addActivity } = useActivitiesMutations();
  const { loading, error, run } = useMutationState();

  /**
   * Cancels a booking by archiving status and logging activities for all occupants.
   */
  const cancelBooking = useCallback(
    async (bookingRef: string, reason?: string): Promise<CancelBookingResult> => {
      if (!database) {
        throw new Error("Database not initialized");
      }

      return await run(async () => {
        // Step 1: Write status="cancelled" to /bookingMeta
        await archiveBooking(bookingRef, reason, "staff");

        // Step 2: Enumerate occupants from /bookings/{bookingRef}
        const bookingRef_ = ref(database, `bookings/${bookingRef}`);
        const snapshot = await get(bookingRef_);

        if (!snapshot.exists()) {
          throw new Error(`Booking ${bookingRef} not found`);
        }

        const occupantsData = snapshot.val() as Record<string, unknown>;
        const occupantIds = Object.keys(occupantsData);

        // Step 3: Log activity code 27 for each occupant.
        // Do not silently accept failures: collect and surface every failed occupant.
        const activityResults = await Promise.all(
          occupantIds.map(async (occupantId) => {
            try {
              const result = await addActivity(occupantId, 27);
              return { occupantId, result };
            } catch (activityErr) {
              const message =
                activityErr instanceof Error
                  ? activityErr.message
                  : "Unknown activity failure";
              return {
                occupantId,
                result: { success: false, error: message },
              };
            }
          })
        );

        const activityFailures: CancellationActivityFailure[] = activityResults
          .filter((entry) => !entry.result.success)
          .map((entry) => ({
            occupantId: entry.occupantId,
            error: entry.result.error ?? "Failed to log cancellation activity",
          }));

        if (activityFailures.length > 0) {
          throw new CancellationPartialFailureError(bookingRef, activityFailures);
        }

        // Step 4: guestsByBooking data remains intact (no deletion)
        return {
          success: true,
          bookingRef,
          archived: true,
          activityFailures: [],
        };
      });
    },
    [database, archiveBooking, addActivity, run]
  );

  return {
    cancelBooking,
    loading,
    error,
  };
}
